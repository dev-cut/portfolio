'use client';

import { motion, UseInViewOptions } from 'framer-motion';
import { ReactNode } from 'react';

interface FadeInProps {
    children: ReactNode;
    delay?: number;
    direction?: 'up' | 'down' | 'left' | 'right';
    fullWidth?: boolean;
    className?: string;
    viewport?: UseInViewOptions;
}

export default function FadeIn({
    children,
    delay = 0,
    direction = 'up',
    fullWidth = false,
    className = '',
    viewport = { once: true, margin: '-50px' },
}: FadeInProps) {
    const directions = {
        up: { y: 40, x: 0 },
        down: { y: -40, x: 0 },
        left: { x: 40, y: 0 },
        right: { x: -40, y: 0 },
    };

    return (
        <motion.div
            initial={{
                opacity: 0,
                ...directions[direction],
            }}
            whileInView={{
                opacity: 1,
                x: 0,
                y: 0,
            }}
            viewport={viewport}
            transition={{
                duration: 0.7,
                delay: delay,
                ease: [0.21, 0.47, 0.32, 0.98], // 부드러운 커스텀 이징
            }}
            className={className}
            style={{ width: fullWidth ? '100%' : undefined }}
        >
            {children}
        </motion.div>
    );
}
